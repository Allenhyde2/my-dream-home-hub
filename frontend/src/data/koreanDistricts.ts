export function searchDong(
  query: string
): { city: string; district: string; dong: string }[] {
  const results: { city: string; district: string; dong: string }[] = [];
  const lowerQuery = query.toLowerCase();

  for (const cityData of KOREAN_DISTRICTS) {
    for (const district of cityData.districts) {
      for (const dong of district.dongs) {
        if (dong.toLowerCase().includes(lowerQuery)) {
          results.push({
            city: cityData.city,
            district: district.name,
            dong: dong,
          });
        }
      }
    }
  }

  return results.slice(0, 10);
}

export function getDistrictsByCity(
  city: string
): { name: string; dongs: string[] }[] {
  const cityData = KOREAN_DISTRICTS.find((c) => c.city === city);
  return cityData?.districts || [];
}

export function getDistrictNamesByCity(city: string): string[] {
  const cityData = KOREAN_DISTRICTS.find((c) => c.city === city);
  return cityData?.districts.map((d) => d.name) || [];
}

export function getDongsByDistrict(city: string, district: string): string[] {
  const cityData = KOREAN_DISTRICTS.find((c) => c.city === city);
  if (!cityData) return [];
  const districtData = cityData.districts.find((d) => d.name === district);
  return districtData?.dongs || [];
}

export function getCities(): string[] {
  return KOREAN_DISTRICTS.map((c) => c.city);
}

export function searchAddress(
  query: string
): {
  city: string;
  district: string;
  dong: string;
  matchType: "full" | "partial";
}[] {
  if (!query.trim()) return [];
  const results: {
    city: string;
    district: string;
    dong: string;
    matchType: "full" | "partial";
    score: number;
  }[] = [];
  const maxResults = 20;

  const tokens = query.trim().split(/\s+/).filter(Boolean);

  for (const cityData of KOREAN_DISTRICTS) {
    const cityMatch = tokens.some(token => cityData.city.includes(token));

    for (const district of cityData.districts) {
      const districtMatch = tokens.some(token => district.name.includes(token));

      for (const dong of district.dongs) {
        const dongMatch = tokens.some(token => dong.includes(token));
        const dongExact = tokens.some(token => dong === token);

        let score = 0;
        let matchType: "full" | "partial" = "partial";

        if (cityMatch && districtMatch && dongMatch) {
          matchType = "full";
          score = 100 + (dongExact ? 10 : 0);
        } else if (districtMatch && dongMatch) {
          matchType = "full";
          score = 90 + (dongExact ? 10 : 0);
        } else if (dongMatch) {
          // 읍/면/동 이름만으로 검색 가능
          score = 80 + (dongExact ? 10 : 0);
          matchType = dongExact ? "full" : "partial";
        } else if (cityMatch && districtMatch) {
          score = 60;
        } else if (districtMatch) {
          score = 50;
        } else {
          continue;
        }

        results.push({
          city: cityData.city,
          district: district.name,
          dong,
          matchType,
          score,
        });
      }
    }
  }

  results.sort((a, b) => b.score - a.score);

  return results.slice(0, maxResults).map(({ score, ...rest }) => rest);
}
